#include <unistd.h>

unsigned int	ft_strlcat(char *dest, char *scr, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (scr[j] && j < size)
	{
		dest[i + j] = scr[j];
		j++;
	}
	dest[i + j] = '\0';
	return (j);
}
